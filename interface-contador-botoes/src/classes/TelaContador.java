    private void btnConteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConteActionPerformed
        // TODO add your handling code here:
        int in = sliInicio.getValue();
        int fm = sliFim.getValue();
        int ps = sliPasso.getValue();
        
        DefaultListModel lista = new DefaultListModel();
        
        for (int c = in; c <= fm; c += ps){
            lista.addElement(c);
        }
        lstConte.setModel(lista);
        
    }//GEN-LAST:event_btnConteActionPerformed

